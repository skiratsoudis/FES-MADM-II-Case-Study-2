# -----------------------------
# Helper: CAIE heatmap (NO shadow; white bold numbers)
# -----------------------------
plot_heatmap <- function(df, value_col, title, subtitle, fill_title, fmt="%.3f") {
  df <- df %>%
    mutate(
      Criterion = paste0(as.character(CriterionCode), ": ", crit_labels[as.character(CriterionCode)]),
      Criterion = factor(Criterion, levels = paste0(crit_levels, ": ", crit_labels[crit_levels]))
    )
  ggplot(df, aes(x = Scenario, y = Criterion, fill = .data[[value_col]])) +
    geom_tile(color="white", linewidth=0.4) +
    geom_text(aes(label = sprintf(fmt, .data[[value_col]])),
              color="white", fontface="bold", size=4.8) +
    scale_fill_gradient(low="grey35", high="grey10", name=fill_title) +
    labs(title=title, subtitle=subtitle, x=NULL, y=NULL) +
    theme_caie() +
    theme(axis.text.x = element_text(angle=25, hjust=1))
}

